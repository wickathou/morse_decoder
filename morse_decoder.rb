  '.....' => '5',
  '-....' => '6',
  '--...' => '7',
  '---..' => '8',
  '----.' => '9'
}.freeze

def decode_char(randomChar)
  MORSE[randomChar]
end

def decode_word(randomChar)
  randomChar.split.map { |string| decode_char string }.join
end

def decode_sentence(randomChar)
  words = randomChar.split('   ')
  puts words.map { |word| decode_word word }.join(' ')
end

